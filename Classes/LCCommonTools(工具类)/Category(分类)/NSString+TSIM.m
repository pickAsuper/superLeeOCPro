char tsim_pfl(unsigned short hanzi)
{
    int index = hanzi - HANZI_START;
    if (index >= 0 && index <= HANZI_COUNT)
    {
        return tsim_firstLetterArray[index];
    }
    else
    {
        return '#';
    }
}


@implementation NSString (TSIM)

+ (NSString *)tsim_getInitialByString:(NSString *)str
{
    NSString *trimedString = [str stringByTrimmingCharactersInSet:[NSCharacterSet whitespaceAndNewlineCharacterSet]];
    if ([NSString isNullOrEmpty:trimedString]) {
        return @"#";
    }
    unichar f = [[trimedString uppercaseString] characterAtIndex:0];
    if ((int)f >= 65 && (int)f <= 90) {
        return [NSString stringWithFormat:@"%c", f];
    } else {
        char tmp = tsim_pfl(f);
        if (((int)tmp >= 65 && (int)tmp <= 90) || ((int)tmp >= 96 && (int)tmp <= 122)) {
            return [[NSString stringWithFormat:@"%c", tmp]uppercaseString];
        } else {
            return @"#";
        }
    }
}

